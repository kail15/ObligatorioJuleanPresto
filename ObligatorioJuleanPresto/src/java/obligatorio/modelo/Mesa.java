
package obligatorio.modelo;

public class Mesa {
    private int numero;
    private boolean estado;

    public Mesa(int numero) {
        this.numero = numero;
    }    

    public int getNumero() {
        return numero;
    }

    public void setNumero(int numero) {
        this.numero = numero;
    }   

    public boolean getEstado() {
        return estado;
    }

    public void setEstado(boolean estado) {
        this.estado = estado;
    }
    
    
    
    
}
